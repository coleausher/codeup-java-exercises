public class StringExercise {
    public static void main(String[] args) {

        String pink = "We don't need no education";
        String floyd = "We don't need no thought control";
        System.out.println(pink + " " + floyd);

        String check = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(check);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(backslash);
    }
}

